// Utilizes onmouseover and onmouseout events, makes images larger and resets them when mouseover status is over
function imageEnlargeBow(x) {
    x.style.height="60px";
    x.style.width="25px";
    }
   function imageResetBow(x) {
   
    x.style.height="40px";
    x.style.width="15px";
    }

